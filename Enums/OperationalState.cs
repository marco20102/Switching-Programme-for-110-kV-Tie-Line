namespace Switching_Programme_for_110_kV_Tie_Line.Enums;


// Оперативний стан елемента мережі (наприклад, лінії, трансформатора)
public enum OperationalState
{
    InService = 0,    // У роботі: під напругою, передає потужність
    OutOfService = 1, // Виведено з роботи: резерв, готово до вмикання
    Isolated = 2,     // Ізольовано: відокремлено, є видимі розриви
    Earthed = 3,      // Заземлено: накладено заземлення, дозволено роботи
    Maintenance = 4,  // У ремонті: технічне обслуговування, випробування
    Switching = 5     // Тривають перемикання (тимчасовий стан)
}