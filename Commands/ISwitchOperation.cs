namespace Switching_Programme_for_110_kV_Tie_Line.Commands;


// Інтерфейс "Команда" (Command)
// https://refactoring.guru/uk/design-patterns/command

// Мета - перетворити запит (оперативну дію) на окремий об'єкт. 
// Це дозволяє відокремити об'єкт, який ініціює команду (черговий диспетчер
// або автоматизована система диспетчерського керування), від об'єкта,
// який її виконує (вимикач/роз'єднувач).

// Плюси:
// 1. Принцип єдиного обов'язку (SRP): класи, що ініціюють дії, відділені
// від класів, що ці дії виконують.
// 2. Принцип відкритості/закритості (OCP): можна додавати нові типи команд
// (наприклад, "Замір струму"), не змінюючи існуючий код.
// 3. Undo/Redo: легко реалізувати скасування операцій, що життєво важливо
// для безпеки перемикань.
// 4. Макрокоманди: дозволяє збирати прості дії у складні ланцюжки
// (бланки перемикань).

// Мінуси:
// 1. Ускладнення коду: створюється багато додаткових класів для кожної
// окремої операції.
// 2. Зайвий рівень абстракції: для дуже простих систем це може бути
// "overengineering" (надмірне проєктування).

public interface ISwitchOperation
{
    void Execute();  // виконати оперативну дію

    void Undo();     // скасувати оперативну дію
}

// У цій програмі для транзитної лінії мінус ускладнення коду нівелюється тим,
// що безпека (Undo) та гнучкість (Макрокоманди) є пріоритетом.
// В енергетиці краще мати чітку структуру об'єктів-команд, ніж один
// гігантський метод з сотнею if-else перевірок.